public class math {
    public static void main(String[] args){
        int x = 7;
        int y = 3;
        int total;

        total = x + y;
        System.out.println(total);

        total = x - y;
        System.out.println(total);

        total = x * y;
        System.out.println(total);

        total = x / y;
        System.out.println(total);

        total = x % y;
        System.out.println(total);

        y++;
        System.out.println(y);

        x--;
        System.out.println(x);
    }
}
